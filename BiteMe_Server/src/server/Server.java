package server;

/**
 * The Class Server entity.
 */
public class Server {
	
	/** The ip. */
	private String IP;
	
	/** The Port. */
	private int Port;
	
	/**
	 * Gets the ip.
	 *
	 * @return the ip
	 */
	public String getIP() {
		return IP;
	}
	
	/**
	 * Sets the ip.
	 *
	 * @param iP the new ip
	 */
	public void setIP(String iP) {
		IP = iP;
	}
	
	/**
	 * Gets the port.
	 *
	 * @return the port
	 */
	public int getPort() {
		return Port;
	}
	
	/**
	 * Sets the port.
	 *
	 * @param port the new port
	 */
	public void setPort(int port) {
		Port = port;
	}
	
}
