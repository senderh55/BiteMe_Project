package delivery;

/**
 * The Class Delivery represents delivery in BiteMe system.
 */
public class Delivery {
	
	/** The order receiver name. */
	String receiverName;

	/** The user phone number. */
	String phoneNumber;
	
	/** The user email. */
	String email;
	
	/** The user street. */
	String street;
	
	/** The user city. */
	String city;
	
	/** The user zip code. */
	String zipCode;
	
	/** The user apartment. */
	String apartment;
	
	/** The user building floor. */
	String floor;
	
	/** The user building code. */
	String buildingCode;

	String businessName;
	

	/**
	 * Gets the city.
	 *
	 * @return String represents the city name
	 */
	public String getCity() {
		return city;
	}

	/**
	 * Sets the city.
	 *
	 * @param city - String represents the new city name
	 */
	public void setCity(String city) {
		this.city = city;
	}

	/**
	 * Gets the zip code.
	 *
	 * @return String represents the zip code
	 */
	public String getZipCode() {
		return zipCode;
	}

	/**
	 * Sets the zip code.
	 *
	 * @param zipCode - String represents the new zip code
	 */
	public void setZipCode(String zipCode) {
		this.zipCode = zipCode;
	}

	/**
	 * Gets the apartment.
	 *
	 * @return String represents the apartment
	 */
	public String getApartment() {
		return apartment;
	}

	/**
	 * Sets the apartment.
	 *
	 * @param apartment - String represents the new apartment
	 */
	public void setApartment(String apartment) {
		this.apartment = apartment;
	}

	/**
	 * Gets the floor.
	 *
	 * @return String represents the floor
	 */
	public String getFloor() {
		return floor;
	}

	/**
	 * Sets the floor.
	 *
	 * @param floor - String represents the new floor
	 */
	public void setFloor(String floor) {
		this.floor = floor;
	}

	/**
	 * Gets the building code.
	 *
	 * @return String represents the building code
	 */
	public String getBuildingCode() {
		return buildingCode;
	}

	/**
	 * Sets the building code.
	 *
	 * @param buildindCode - String represents the new building code
	 */
	public void setBuildingCode(String buildindCode) {
		this.buildingCode = buildindCode;
	}

	/**
	 * Gets the receiver name.
	 *
	 * @return String represents the new receiver name
	 */
	public String getReceiverName() {
		return receiverName;
	}

	/**
	 * Sets the receiver name.
	 *
	 * @param receiverName - String represents the new receiver name
	 */
	public void setReceiverName(String receiverName) {
		this.receiverName = receiverName;
	}


	/**
	 * Gets the phone number.
	 *
	 * @return String represents the phone number
	 */
	public String getPhoneNumber() {
		return phoneNumber;
	}

	/**
	 * Sets the phone number.
	 *
	 * @param phoneNumber - String represents the new phone number
	 */
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	/**
	 * Gets the email.
	 *
	 * @return String represents the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * Sets the email.
	 *
	 * @param email - String represents the new email
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * Gets the street.
	 *
	 * @return String represents the street name
	 */
	public String getStreet() {
		return street;
	}

	/**
	 * Sets the street.
	 *
	 * @param street - String represents the new street name
	 */
	public void setStreet(String street) {
		this.street = street;
	}


	public String getBusinessName() {
		return businessName;
	}

	public void setBusinessName(String businessName) {
		this.businessName = businessName;
	}


}
